#!/usr/bin/env bash
# docs.sh — Deep context document generation for rhino-os projects.
# Scans a codebase, spawns claude -p subagents to produce structured context docs.
# Sourced by bin/rhino alongside config.sh and brains.sh.
#
# Usage (from bin/rhino):
#   source "$RHINO_DIR/bin/lib/docs.sh"
#   generate_all_docs "/path/to/project"
#   refresh_docs "/path/to/project"

_DOCS_DIR_NAME="documents"

_docs_budget() {
    cfg "docs.budget_per_doc" "0.75"
}

_docs_output_dir() {
    local project_dir="$1"
    echo "$project_dir/$_DOCS_DIR_NAME"
}

_docs_meta_file() {
    local project_dir="$1"
    echo "$project_dir/.claude/state/docs-meta.json"
}

_docs_prompt_dir() {
    echo "$RHINO_DIR/agents/docs"
}

# Run a claude -p subagent with a prompt file and project context
_run_doc_agent() {
    local prompt_file="$1"
    local project_dir="$2"
    local output_file="$3"
    local doc_name="$4"
    local budget
    budget=$(_docs_budget)

    if [[ ! -f "$prompt_file" ]]; then
        echo -e "${RED}Missing prompt: $prompt_file${NC}" >&2
        return 1
    fi

    local system_prompt
    system_prompt="$(cat "$prompt_file")"

    echo -e "  ${BLUE}generating${NC} $doc_name..."

    local result
    result=$(CLAUDECODE= claude -p \
        --output-format text \
        --max-budget-usd "$budget" \
        --allowedTools "Read,Glob,Grep,Bash,WebSearch,WebFetch" \
        --system-prompt "$system_prompt" \
        "Generate the $doc_name document for the project at: $project_dir

Output ONLY the markdown document content. No preamble, no code fences wrapping the whole doc.
Start with the # heading." 2>/dev/null)

    if [[ -n "$result" ]]; then
        # Add metadata header
        {
            echo "<!-- Generated by rhino docs | $(date -u +%Y-%m-%dT%H:%M:%SZ) | $(git -C "$project_dir" rev-parse --short HEAD 2>/dev/null || echo 'no-git') -->"
            echo ""
            echo "$result"
        } > "$output_file"
        echo -e "  ${GREEN}wrote${NC} $output_file"
    else
        echo -e "  ${RED}failed${NC} $doc_name (empty output)" >&2
        return 1
    fi
}

# Run an interactive doc agent that interviews the founder
_run_interview_agent() {
    local prompt_file="$1"
    local project_dir="$2"
    local output_file="$3"
    local doc_name="$4"
    local budget
    budget=$(_docs_budget)

    if [[ ! -f "$prompt_file" ]]; then
        echo -e "${RED}Missing prompt: $prompt_file${NC}" >&2
        return 1
    fi

    local system_prompt
    system_prompt="$(cat "$prompt_file")"

    echo -e "  ${BLUE}interviewing${NC} for $doc_name (interactive)..."

    # Interactive — no --output-format text, uses AskUserQuestion
    local result
    result=$(CLAUDECODE= claude -p \
        --max-budget-usd "$budget" \
        --output-format text \
        --allowedTools "Read,Glob,Grep,Bash,AskUserQuestion" \
        --system-prompt "$system_prompt" \
        "Interview the founder and generate the $doc_name document for: $project_dir

Use AskUserQuestion to ask about their users, goals, and vision.
Keep it to 3-5 focused questions — don't over-interview.
Output ONLY the final markdown document. Start with the # heading." 2>/dev/null)

    if [[ -n "$result" ]]; then
        {
            echo "<!-- Generated by rhino docs (interview) | $(date -u +%Y-%m-%dT%H:%M:%SZ) | $(git -C "$project_dir" rev-parse --short HEAD 2>/dev/null || echo 'no-git') -->"
            echo ""
            echo "$result"
        } > "$output_file"
        echo -e "  ${GREEN}wrote${NC} $output_file"
    else
        echo -e "  ${RED}failed${NC} $doc_name (empty output)" >&2
        return 1
    fi
}

# Generate a single doc by type
generate_single_doc() {
    local doc_type="$1"
    local project_dir="${2:-.}"
    project_dir="$(cd "$project_dir" && pwd)"
    local output_dir
    output_dir="$(_docs_output_dir "$project_dir")"
    local prompt_dir
    prompt_dir="$(_docs_prompt_dir)"

    mkdir -p "$output_dir"

    case "$doc_type" in
        platform-docs)
            _run_doc_agent "$prompt_dir/platform-docs.md" "$project_dir" "$output_dir/platform-docs.md" "platform-docs"
            ;;
        architecture)
            _run_doc_agent "$prompt_dir/architecture.md" "$project_dir" "$output_dir/architecture.md" "architecture"
            ;;
        styleguide)
            _run_doc_agent "$prompt_dir/styleguide.md" "$project_dir" "$output_dir/styleguide.md" "styleguide"
            ;;
        icps)
            _run_interview_agent "$prompt_dir/icps.md" "$project_dir" "$output_dir/ICPs.md" "ICPs"
            ;;
        roadmap)
            _run_interview_agent "$prompt_dir/roadmap.md" "$project_dir" "$output_dir/roadmap.md" "roadmap"
            ;;
        *)
            echo -e "${RED}Unknown doc type: $doc_type${NC}" >&2
            echo "Valid types: platform-docs, architecture, styleguide, icps, roadmap" >&2
            return 1
            ;;
    esac
}

# Generate all docs (automated first, then interviews)
generate_all_docs() {
    local project_dir="${1:-.}"
    project_dir="$(cd "$project_dir" && pwd)"
    local output_dir
    output_dir="$(_docs_output_dir "$project_dir")"
    local prompt_dir
    prompt_dir="$(_docs_prompt_dir)"
    local meta_file
    meta_file="$(_docs_meta_file "$project_dir")"
    local success=0
    local failed=0

    mkdir -p "$output_dir" "$(dirname "$meta_file")"

    echo -e "${BOLD}Generating context documents...${NC}"
    echo ""

    # Phase 1: Automated docs (no human input needed)
    echo -e "${BOLD}Phase 1: Codebase scan${NC}"
    for doc_type in platform-docs architecture styleguide; do
        if generate_single_doc "$doc_type" "$project_dir"; then
            ((success++))
        else
            ((failed++))
        fi
    done

    # Phase 2: Interview docs (need human input)
    echo ""
    echo -e "${BOLD}Phase 2: Founder interview${NC}"
    for doc_type in icps roadmap; do
        if generate_single_doc "$doc_type" "$project_dir"; then
            ((success++))
        else
            ((failed++))
        fi
    done

    # Write metadata
    local git_sha
    git_sha=$(git -C "$project_dir" rev-parse HEAD 2>/dev/null || echo "none")
    cat > "$meta_file" << METAEOF
{
  "generated": "$(date -u +%Y-%m-%dT%H:%M:%SZ)",
  "last_sha": "$git_sha",
  "builds_since_refresh": 0,
  "docs_generated": $success,
  "docs_failed": $failed
}
METAEOF

    echo ""
    echo -e "${BOLD}Done:${NC} $success generated, $failed failed"
    echo -e "${DIM}Docs at: $output_dir/${NC}"
}

# Refresh docs incrementally based on git diff
refresh_docs() {
    local project_dir="${1:-.}"
    project_dir="$(cd "$project_dir" && pwd)"
    local meta_file
    meta_file="$(_docs_meta_file "$project_dir")"
    local mode="${2:-auto}"  # "auto" = only changed, "all" = everything

    if [[ ! -f "$meta_file" ]]; then
        echo -e "${YELLOW}No docs metadata — running full generation${NC}"
        generate_all_docs "$project_dir"
        return
    fi

    if [[ "$mode" == "all" ]]; then
        generate_all_docs "$project_dir"
        return
    fi

    # Get changed files since last generation
    local last_sha
    last_sha=$(grep -o '"last_sha":"[^"]*"' "$meta_file" | cut -d'"' -f4)
    if [[ -z "$last_sha" || "$last_sha" == "none" ]]; then
        echo -e "${YELLOW}No previous SHA — running full generation${NC}"
        generate_all_docs "$project_dir"
        return
    fi

    local changed_files
    changed_files=$(git -C "$project_dir" diff --name-only "$last_sha"..HEAD 2>/dev/null || echo "")
    if [[ -z "$changed_files" ]]; then
        echo -e "${DIM}No changes since last doc generation${NC}"
        return
    fi

    echo -e "${BOLD}Refreshing docs (incremental)...${NC}"
    local refreshed=0

    # Check triggers for each doc type
    local trigger_platform
    trigger_platform=$(cfg "docs.trigger_platform" "page\\.tsx|route\\.ts|endpoint|controller|handler|api")
    local trigger_architecture
    trigger_architecture=$(cfg "docs.trigger_architecture" "schema|model|migration|prisma|drizzle|api|types")
    local trigger_styleguide
    trigger_styleguide=$(cfg "docs.trigger_styleguide" "tailwind|css|theme|color|font|style|globals")

    if echo "$changed_files" | grep -qiE "$trigger_platform"; then
        generate_single_doc "platform-docs" "$project_dir"
        ((refreshed++))
    fi
    if echo "$changed_files" | grep -qiE "$trigger_architecture"; then
        generate_single_doc "architecture" "$project_dir"
        ((refreshed++))
    fi
    if echo "$changed_files" | grep -qiE "$trigger_styleguide"; then
        generate_single_doc "styleguide" "$project_dir"
        ((refreshed++))
    fi

    if [[ "$refreshed" -eq 0 ]]; then
        echo -e "${DIM}Changes didn't match any doc triggers — skipping refresh${NC}"
    else
        echo -e "${GREEN}Refreshed $refreshed doc(s)${NC}"
    fi

    # Update metadata
    local git_sha
    git_sha=$(git -C "$project_dir" rev-parse HEAD 2>/dev/null || echo "none")
    local current_meta
    current_meta=$(cat "$meta_file")
    # Simple sed replacements (macOS compatible)
    echo "$current_meta" | sed "s/\"last_sha\":\"[^\"]*\"/\"last_sha\":\"$git_sha\"/" | sed 's/"builds_since_refresh":[0-9]*/"builds_since_refresh":0/' > "$meta_file"
}

# Check if docs should auto-refresh (called from build loop)
maybe_refresh_docs() {
    local project_dir="${1:-.}"
    local meta_file
    meta_file="$(_docs_meta_file "$project_dir")"

    [[ ! -f "$meta_file" ]] && return 0

    local refresh_interval
    refresh_interval=$(cfg "docs.refresh_interval" 5)
    local builds_since
    builds_since=$(grep -o '"builds_since_refresh":[0-9]*' "$meta_file" | grep -o '[0-9]*')
    builds_since="${builds_since:-0}"

    if (( builds_since >= refresh_interval )); then
        echo -e "${BLUE}Auto-refreshing docs (every $refresh_interval builds)...${NC}"
        refresh_docs "$project_dir"
    else
        # Increment counter
        local new_count=$((builds_since + 1))
        sed -i '' "s/\"builds_since_refresh\":$builds_since/\"builds_since_refresh\":$new_count/" "$meta_file" 2>/dev/null || \
        sed -i "s/\"builds_since_refresh\":$builds_since/\"builds_since_refresh\":$new_count/" "$meta_file" 2>/dev/null || true
    fi
}

# Inject document references into CLAUDE.md
inject_docs_into_claude_md() {
    local project_dir="${1:-.}"
    local claude_md="$project_dir/CLAUDE.md"
    local docs_dir="$project_dir/$_DOCS_DIR_NAME"

    [[ ! -f "$claude_md" ]] && return 0
    [[ ! -d "$docs_dir" ]] && return 0

    # Check if already injected
    if grep -q "## Context Documents" "$claude_md" 2>/dev/null; then
        return 0
    fi

    # Append reference section
    {
        echo ""
        echo "## Context Documents"
        echo "Deep context generated by \`rhino docs\`. Read these for full project understanding:"
        for doc in "$docs_dir"/*.md; do
            [[ ! -f "$doc" ]] && continue
            local docname
            docname=$(basename "$doc")
            echo "- @$_DOCS_DIR_NAME/$docname"
        done
    } >> "$claude_md"

    echo -e "  ${GREEN}updated CLAUDE.md with document references${NC}"
}
